const msgTextarea = document.querySelector("#msg-textarea");
msgTextarea.addEventListener("keyup", (e) => {
  e.preventDefault();

  const value = e.target.textContent;

  if (e.key !== 'Enter') return;

  e.target.textContent = '';

  if (!chatInfo.current || !value) return;
  chatInfo.current.chatMsgs.push({
    name: USER_INFO.name,
    self: true,
    date: new Date().getDate(),
    src: USER_INFO.src,
    message: value
  });
  renderChatMsgs(chatInfo.current.chatMsgs, "#chat");
});

function renderChatList(chatList, parentSelector) {
  const parent = document.querySelector(parentSelector);
  const liList = [];

  chatList.forEach((chat) => {
    const li = document.createElement('li');

    li.onclick = () => {
      chatInfo.current = chat;
      liList.forEach(li2 => {
        if (li2 === li) {
          li2.classList.toggle('bg-[#40444B]');
          li2.classList.toggle('text-[#ffffff]');
        } else {
          li2.classList.remove('bg-[#40444B]');
          li2.classList.remove('text-[#ffffff]');
        }
      });
    }

    li.className = `flex items-center px-2 py-1 lg:px-4 lg:py-2 rounded-md text-[#8F9398] text-xs lg:text-xl lowercase align-middle cursor-pointer duration-300 hover:bg-[#40444B] hover:text-[#ffffff]`;

    li.innerHTML = `<span class="text-xs lg:text-2xl mr-4 font-bold text-[#8F9398]">#</span>`;

    const span = document.createElement('span');
    span.textContent = chat.name;
    li.appendChild(span);

    parent.appendChild(li);
    liList.push(li);
  });
}

function renderChatMsgs(chatMsgs, parentSelector) {
  const parent = document.querySelector(parentSelector);
  parent.innerHTML = '';

  chatMsgs.forEach((chatMsg) => {
    parent.append(renderChatMsg(chatMsg));
  });
  parent.scrollTop = parent.scrollHeight;
}

function renderChatMsg(chatMsg) {
  const container = document.createElement('div');

  container.className = `${chatMsg.self && 'float-right flex-row-reverse bg-[#ffffff]/[0.1]'} p-2 rounded-xl flex max-w-[60%] w-[60%] mt-5 first:mt-0 overflow-hidden`;

  const imgContainer = document.createElement('div');
  imgContainer.className = `w-10 h-10 min-w-[40px] rounded-full overflow-hidden ${chatMsg.self ? 'ml-6' : 'mr-6'}`;

  const img = document.createElement('img');
  img.className = `w-10 h-10 object-cover`;
  img.src = chatMsg.src;

  imgContainer.appendChild(img);
  container.appendChild(imgContainer);

  const dataContainer = document.createElement('div');
  dataContainer.className = `grow overflow-hidden`;

  const infoContainer = document.createElement('div');

  const nameTag = document.createElement('span');
  nameTag.className = `text-lime-600 mr-3`;
  nameTag.textContent = chatMsg.name;
  infoContainer.appendChild(nameTag);

  const dateTag = document.createElement('span');
  dateTag.className = `text-[#8F9398] text-xs semibold tracking-wider`;
  dateTag.textContent = chatMsg.date;
  infoContainer.appendChild(dateTag);

  dataContainer.appendChild(infoContainer);

  const msgTag = document.createElement('div');
  msgTag.className = `text-[#8F9398] break-words`;
  msgTag.textContent = chatMsg.message;
  dataContainer.appendChild(msgTag);

  container.appendChild(dataContainer);

  return container;
}